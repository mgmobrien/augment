import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import { execSync } from "child_process";

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
*/
`;

const watch = process.argv[2] === "watch";
const debug = watch || process.argv[2] === "develop";
const buildId = new Date().toISOString();
let gitSha = "nogit";
try {
  gitSha = execSync("git rev-parse --short HEAD", { encoding: "utf8" }).trim();
} catch {
  // Non-git environments keep a stable fallback marker.
}

const PLUGIN_DIR =
  "/Users/mattobrien/Obsidian Main Vault/ObsidianVault/.obsidian/plugins/augment-terminal";

// In watch mode, output directly to the plugin dir so Obsidian hot-reload
// picks up changes immediately. Build mode outputs to repo root (committed).
const outfile = watch ? `${PLUGIN_DIR}/main.js` : "main.js";

const context = await esbuild.context({
  banner: { js: banner },
  entryPoints: ["src/main.ts"],
  bundle: true,
  loader: { ".css": "text" },
  external: [
    "obsidian",
    "electron",
    "@codemirror/autocomplete",
    "@codemirror/collab",
    "@codemirror/commands",
    "@codemirror/language",
    "@codemirror/lint",
    "@codemirror/search",
    "@codemirror/state",
    "@codemirror/view",
    "@lezer/common",
    "@lezer/highlight",
    "@lezer/lr",
    ...builtins,
  ],
  format: "cjs",
  target: "es2018",
  logLevel: "info",
  sourcemap: debug ? "inline" : false,
  treeShaking: true,
  define: {
    __AUGMENT_BUILD_ID__: JSON.stringify(buildId),
    __AUGMENT_GIT_SHA__: JSON.stringify(gitSha),
  },
  outfile,
});

if (watch) {
  await context.watch();
} else {
  await context.rebuild();
  process.exit(0);
}
